public class SwapNum{
    public static void main(String[] args){
         int a=5, b=7;
         
        System.out.println("Before swapping: a = " + a + ", b = " + b);
         
         //swapping
         int temp =a;
         a=b;
         b=temp;

         System.out.println("after swapping a = " + a + ", b = " + b);
         
 
    }
    


} 